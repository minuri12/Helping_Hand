    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String search = seebox.getText();
        int contact1 = Integer.parseInt(numberbox.getText());
        SimpleDateFormat myformat = new SimpleDateFormat("yyyy-MM-dd");
        String day = myformat.format(admissionbox.getDate());
        
        if(search.equals("") || day.equals(null)){
            JOptionPane.showMessageDialog(null,"Every Field Is Required");
        }else{
        
        
        try {
            String sql = "UPDATE patients SET  contact=? , addmissiondate=? WHERE name='"+search+"'" ;
            pst= conn.prepareStatement(sql);
            pst.setInt(1,contact1);
            pst.setString(2, day);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"Data Updated Successfully");
            clear();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

