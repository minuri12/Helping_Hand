public void search(){
    String see = seebox.getText();
        try {
            String sql  = "SELECT * FROM  patients WHERE name= '"+see+"'  ";
            pst = conn.prepareStatement(sql);
            rst=pst.executeQuery();
            while(rst.next()){
              
                    numberbox.setText(String.format("0"+rst.getString("contact")));
                    SimpleDateFormat myformat = new SimpleDateFormat("yyyy-MM-dd");    
                    java.sql.Date day = rst.getDate("addmissiondate");
                    admissionbox.setDate(day);
     
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
}
